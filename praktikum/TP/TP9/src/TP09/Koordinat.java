package TP09;

public class Koordinat {
    private int x;
    private int y;

    // Konstruktor
    public Koordinat(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter untuk x
    public int getX() {
        return x;
    }

    // Getter untuk y
    public int getY() {
        return y;
    }

    // Setter untuk x
    public void setX(int x) {
        this.x = x;
    }

    // Setter untuk y
    public void setY(int y) {
        this.y = y;
    }
}

