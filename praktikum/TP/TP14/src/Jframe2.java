    private void editbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbuttonActionPerformed
        // TODO add your handling code here:
        try{
            Database db = new Database();
            String sql = "select * from tabel_mahasiswa where nim = "+this.nim;
            ResultSet rs = db.getData(sql);
            if (rs.next()){
                namafielddialog.setText(rs.getString("nama"));
                NIMfielddialog.setText(rs.getString("nim"));
                if("IF".equals(rs.getString("prodi"))) {
                    ifbuttondialog.setSelected(true);
                }else if ("IT".equals(rs.getString("prodi"))) {
                    itbuttondialog.setSelected(true);
                }else if ("SE".equals(rs.getString("prodi"))) {
                    sebuttondialog.setSelected(true);
                }else {
                    dsbuttondialog.setSelected(true);
                };
                this.id = rs.getInt("id"); 
                System.out.println(this.id);
            };
               
        }catch (SQLException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        };
        jDialog1.setVisible(true);
        
    }//GEN-LAST:event_editbuttonActionPerformed

