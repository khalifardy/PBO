    private void savebuttondialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttondialogActionPerformed
        // TODO add your handling code here:
        String prodi="";
        if (ifbuttondialog.isSelected()){
            prodi = ifbuttondialog.getText();
        };

        if (itbuttondialog.isSelected()){
            prodi = itbuttondialog.getText();
        };

        if (sebuttondialog.isSelected()){
            prodi = sebuttondialog.getText();
        };
        
        if (dsbuttondialog.isSelected()){
            prodi = dsbuttondialog.getText();
        };

       
       String nama = namafielddialog.getText();
       String nim = NIMfielddialog.getText();
        try{
                Database db = new Database();
                String sql = "UPDATE tabel_mahasiswa SET nama=?, nim=?, prodi=? WHERE id=?";
                
                db.query_update(sql,nama,nim,prodi,this.id);
                db.closeConn();
                      
            }
            catch(SQLException ex){
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null,"Mahasiswa berhasil diupdate");
            namafielddialog.setText("");
            NIMfielddialog.setText("");
            dsbuttondialog.setSelected(false);
            itbuttondialog.setSelected(false);
            sebuttondialog.setSelected(false);
            ifbuttondialog.setSelected(false);
            jDialog1.setVisible(false);
            dispose();
            
            
        
    }//GEN-LAST:event_savebuttondialogActionPerformed

